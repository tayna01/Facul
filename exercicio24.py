a = int(input("Informe o valor um: "))
b = int(input("Informe o valor dois: "))
c = int(input("Informe o valor três: "))
d = int(input("Informe o valor quatro: "))

valor_maior = max(a, b, c, d)
valor_menor = min(a, b, c, d)

print(f"O maior número é: {valor_maior} e o menor número é: {valor_menor}")



